package Java_Programs.Function_Programs;

public class Function_Prog1 {
    public static void main(String[] args) {
        sayhello();
    }

    // No Return type with No parameter
    static void sayhello(){
        System.out.println("Hello!!..This is No Return type with No parameter function");
    }
}
/*
OUTPUT:
Hello!!..This is No Return type with No parameter function
 */