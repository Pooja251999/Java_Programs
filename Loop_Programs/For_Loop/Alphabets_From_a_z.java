package Java_Programs.Loop_Programs.For_Loop;

public class Alphabets_From_a_z {
    public static void main(String[] args) {
        for(char c='a';c<='z';c++){
            System.out.print(c+ "  ");
        }
    }
}
/*
OUTPUT:
a  b  c  d  e  f  g  h  i  j  k  l  m  n  o  p  q  r  s  t  u  v  w  x  y  z
 */